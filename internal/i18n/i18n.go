// Lightweight i18n bundle. Supported locales: "en", "ru".
// The web UI asks for the whole dictionary via /api/i18n so it can render
// without a round-trip per string.
package i18n

import "strings"

type Bundle map[string]string

// Language is a selectable UI locale (code + native display name).
type Language struct {
	Code string `json:"code"`
	Name string `json:"name"`
}

// localeOrder is the order languages appear in the picker. Every locale is
// fully translated; Get still overlays on English defensively so a newly added
// key can never surface as a raw key before its translations land.
// Moldovan uses code "md" (with a "ro" backward-compat alias in normalize).
var localeOrder = []Language{
	{"en", "English"},
	{"ru", "Русский"},
	{"es", "Español"},
	{"fr", "Français"},
	{"de", "Deutsch"},
	{"it", "Italiano"},
	{"pt", "Português"},
	{"md", "Moldovenească"},
	{"zh", "中文"},
	{"ja", "日本語"},
	{"ko", "한국어"},
}

var locales = map[string]Bundle{
	"en": en, "ru": ru, "es": es, "fr": fr, "de": de, "it": it,
	"pt": pt, "md": md, "zh": zh, "ja": ja, "ko": ko,
}

// normalize maps legacy/alias codes to their canonical locale code. "ro" was the
// original code for the Moldovan bundle before it moved to "md"; keep resolving
// it so anyone who already saved "ro" doesn't silently drop to English.
func normalize(code string) string {
	code = strings.ToLower(code)
	if code == "ro" {
		return "md"
	}
	return code
}

// Name returns the native display name for a locale code (empty if unknown).
func Name(code string) string {
	code = normalize(code)
	for _, l := range localeOrder {
		if l.Code == code {
			return l.Name
		}
	}
	return ""
}

// Languages returns the selectable languages in display order.
func Languages() []Language { return append([]Language(nil), localeOrder...) }

func Supported() []string {
	out := make([]string, 0, len(localeOrder))
	for _, l := range localeOrder {
		out = append(out, l.Code)
	}
	return out
}

// Get returns the dictionary for a locale. Non-English locales are returned as
// English overlaid with whatever the locale translates, so a partially
// translated language never shows raw keys — missing strings fall back to
// English transparently.
func Get(code string) Bundle {
	code = normalize(code)
	loc, ok := locales[code]
	if !ok || code == "en" {
		return en
	}
	m := make(Bundle, len(en))
	for k, v := range en {
		m[k] = v
	}
	for k, v := range loc {
		m[k] = v
	}
	return m
}

